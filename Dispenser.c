#include <stdio.h>

int main(void) {
    int num;
    printf("Pick a number:");
    scanf("%d", &num);
    switch (num) {
        case 1:
            printf("You choose FuzeTea. \n");
            break;
        case 2:
            printf("You choose Orangina. \n");
            break;
        case 3:
            printf("You choose Apple juice. \n");
            break;
        case 10:
            printf("You choose Chocolate milk. \n");
            break;
        case 11:
            printf("You choose coffee. \n");
            break;
        default:
            printf("Error: no matching number could be found. \n");
    }
    return 0;
}
